/**
 * add - adds integers
 * @a: first int
 * @b: second int
 * Return: INTEGER (Success)
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - performs subtraction integers
 * @a: first int
 * @b: second int
 * Return: INTEGER (Success)
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies integers
 * @a: first int
 * @b: second int
 * Return: INTEGER (Success)
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides integers
 * @a: first int
 * @b: second int
 * Return: INTEGER (Success)
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the mod of two integers
 * @a: first int
 * @b: second int
 * Return: INTEGER (Success)
 */

int mod(int a, int b)
{
	return (a % b);
}
